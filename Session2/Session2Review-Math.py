# The + symbol represents addition
myAdd = 2 + 3 
print("2 + 3 = " + str(myAdd))

# The - symbol represents subtraction
mySub = 5 - 2 
print("5 - 2 = " + str(mySub))

# the * symbol represents multiplication
myMul = 2 * 6 
print("2 * 6 = " + str(myMul))

# the / symbol represents division
myDiv = 9 / 3 
print("9 / 3 = " + str(myDiv))

# the % symbol represents modulo (getting the remainder)
myMod = 7 % 3 
print("7 % 3 = " + str(myMod))