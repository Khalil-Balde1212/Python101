myNum = 20

# == asks if the numbers are equal
if(myNum == 0):
    print("My number is 0")
else:
    print("My number is not 0")

# <= asks if myNum is less than or equal to -1
# >= asks if myNum is greater than or equal to 1
if(myNum <= -1):
    print("My number is negative!")
    # You do not need to include an else statement with ifs! 
    # Python will assume it will do nothing in the "else" case
if(myNum >= 1):
    print("My number is positive!")

    
# > asks if myNum is greater than 10
if(myNum > 10):
    print("My number is huge!")

